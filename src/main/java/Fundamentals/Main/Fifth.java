package Fundamentals.Main;

import java.util.Scanner;

//5. Ввести число от 1 до 12. Вывести на консоль название месяца, соответствующего данному числу.
// Осуществить проверку корректности ввода чисел.

public class Fifth {
    public static void main(String[] args){
        Scanner scanner = new Scanner(System.in);
        int numberOfMonth;
        System.out.println("Enter a number from 1 to 12");
        if (scanner.hasNextInt()) {
            numberOfMonth = scanner.nextInt();
            switch (numberOfMonth) {
                case 1:
                    System.out.println("January");
                    break;
                case 2:
                    System.out.println("February");
                    break;
                case 3:
                    System.out.println("March");
                    break;
                case 4:
                    System.out.println("April");
                    break;
                case 5:
                    System.out.println("May");
                    break;
                case 6:
                    System.out.println("June");
                    break;
                case 7:
                    System.out.println("July");
                    break;
                case 8:
                    System.out.println("August");
                    break;
                case 9:
                    System.out.println("September");
                    break;
                case 10:
                    System.out.println("October");
                    break;
                case 11:
                    System.out.println("November");
                    break;
                case 12:
                    System.out.println("December");
                    break;
                default:
                    System.out.println("You didn't write digit from defined gap");
            }
        }
        else System.out.println("You didn't write digit");
    }
}
